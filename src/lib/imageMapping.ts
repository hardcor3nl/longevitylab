/**
 * Get image URL for an article based on keywords/tags
 */
export function getArticleImage(tags?: string[], title?: string, description?: string): string {
  if (!tags && !title && !description) return imageMapping['default']

  const searchText = `${tags?.join(' ') || ''} ${title || ''} ${description || ''}`.toLowerCase()

  // Check each key in the mapping
  for (const [key, url] of Object.entries(imageMapping)) {
    if (key !== 'default' && searchText.includes(key)) {
      return url
    }
  }

  return imageMapping['default']
}
