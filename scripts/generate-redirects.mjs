/**
 * Generate Cloudflare Pages `_redirects` from affiliateLinks source of truth.
 * Runs at prebuild so edge 302s stay in sync with /go/[product] client fallbacks.
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const linksPath = path.join(__dirname, '..', 'src', 'lib', 'affiliateLinks.ts')
const outPath = path.join(__dirname, '..', 'public', '_redirects')

const raw = fs.readFileSync(linksPath, 'utf-8')

// Parse Record entries: 'slug': 'https://...'
const entries = []
const re = /['"]([a-z0-9][a-z0-9-]*)['"]\s*:\s*['"](https?:[^'"]+)['"]/gi
let m
while ((m = re.exec(raw)) !== null) {
  const slug = m[1]
  const url = m[2]
  // Skip comment-only noise; only real keys
  if (slug && url && !entries.some(e => e.slug === slug)) {
    entries.push({ slug, url })
  }
}

// Cloudflare _redirects: [source] [destination] [code]
// Prefer first-seen slug; aliases map to same destination separately.
const lines = [
  '# Auto-generated by scripts/generate-redirects.mjs — do not edit by hand',
  `# ${entries.length} affiliate go-links · regenerated on every build`,
  '',
]

for (const { slug, url } of entries) {
  // Spaces in destination must be encoded; Cloudflare treats space as field separator
  const dest = url.replace(/ /g, '%20')
  lines.push(`/go/${slug} ${dest} 302`)
}

fs.writeFileSync(outPath, lines.join('\n') + '\n')
console.log(`_redirects: ${entries.length} go-links written`)
